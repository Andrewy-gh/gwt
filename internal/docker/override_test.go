func TestSanitizeBranchName(t *testing.T) {
	tests := []struct {
		name     string
		input    string
		expected string
	}{
		{"Simple branch", "feature", "feature"},
		{"With slash", "feature/auth", "feature-auth"},
		{"With backslash", "feature\\auth", "feature-auth"},
		{"With spaces", "feature auth", "feature-auth"},
		{"With underscores", "feature_auth", "feature-auth"},
		{"Mixed case", "Feature/Auth", "feature-auth"},
		{"Multiple slashes", "feat/sub/auth", "feat-sub-auth"},
		{"Leading/trailing dash", "-feature-", "feature"},
	}

	for _, tt := range tests {
		t.Run(tt.name, func(t *testing.T) {
			result := sanitizeBranchName(tt.input)
			if result != tt.expected {
				t.Errorf("sanitizeBranchName(%s) = %s, expected %s", tt.input, result, tt.expected)
			}
		})
	}
}

